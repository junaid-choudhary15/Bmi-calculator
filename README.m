BMI Calculator with React Native expo cli
Install npm modules
if you are beginner then must check  https://reactnative.dev/docs/getting-started

if you installed expo CLI then run the commans 'npm install'
'expo start'
download the expo app from play store and scan the QR code

As this project implemented with android so run below command to run application

react-native run-android

As this project implemented with ios so run below command to run application

react-native run-ios 
